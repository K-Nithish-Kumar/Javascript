// string conversion
var value = 100;
console.log(typeof(value));
value = String(value);
console.log(typeof(value));

let valueOne = 100;
console.log(typeof(valueOne));
valueOne = String(valueOne);
console.log(typeof(valueOne));

const valueTwo = 100;
console.log(typeof(valueTwo));

// Numeric conversion
var valueThree = "100";
console.log(typeof(valueThree));
valueThree = Number(valueThree);
console.log(typeof(valueThree));

let valueFour = "100";
console.log(typeof(valueFour));
valueFour = Number(valueFour);
console.log(typeof(valueFour));

const valueFive = "100";
console.log(typeof(valueFive));


// boolean conversion

var valueSix = "True";
console.log(typeof(valueSix));
valueSix = Boolean(valueSix);
console.log(typeof(valueSix));

let valueSeven = 1;
console.log(typeof(valueSeven));
valueSeven = Boolean(valueSeven);
console.log(typeof(valueSeven));

const valueEight = false;
console.log(typeof(valueEight));
