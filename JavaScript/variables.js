// var declarations -> it is in global scope
var myName;
myName = 'Nithish Kumar'
console.log(myName);

// let declarations -> it is in specific scope
let myNameOne;
myNameOne = 'Nithish Kumar'
console.log(myNameOne);

// const declarations -> it is in a specific scope both let and const have the specific scope!
const myNameTwo = 'Nithish kumar';
console.log(myNameTwo);
